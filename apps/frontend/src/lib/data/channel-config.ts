export function getCountryISOCode(countryId: string): string {
  return ISO_MAP[countryId] || countryId;
}

export function getChannelsForCountry(countryId: string): { popular: Channel[]; other: Channel[] } {
  const specific = COUNTRY_CHANNELS[countryId];
  if (specific) return specific;

  // Fallback: common channels for countries without specific config
  const iso = getCountryISOCode(countryId);
  return {
    popular: COMMON_POPULAR.map((c) =>
      c.id === "google_shopping" ? { ...c, id: `google_shopping_${iso.toLowerCase()}`, name: `Google Shopping (${iso})` } : c,
    ),
    other: [...COMMON_OTHER].sort((a, b) => a.name.localeCompare(b.name)),
  };
}
